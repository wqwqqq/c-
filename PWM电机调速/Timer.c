/***********************************************************
功能描述：定时器4初始化  1MS
入口参数：无
返回值：无
************************************************************/
void Timer4Init(void)
{
	T4T3M &= 0xBF;                  //定时器4设置为定时方式
	T4T3M |= 0x20;                  //设置定时器4为1T模式
	T4L = 0xE8;;                     //1T模式下初始装载值 
	T4H = 0xFF;                     //1T模式下初始装载值 
	IE2  |=  (1<<6);	              //使能定时器4中断
	T4T3M |= 0x80;                  //打开定时器4
	EA=1;
} 
